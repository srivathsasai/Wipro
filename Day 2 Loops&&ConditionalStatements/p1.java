public class p1
{
      public static void main(String arg[])
      {
            int a=Integer.parseInt(arg[0]);
            if(a==0)
                    System.out.println("Given Number is ZERO");

            else if(a>0)
                    System.out.println("Given Number is POSITIVE");

            else
                    System.out.println("Given Number is NEGETIVE");
       }
}